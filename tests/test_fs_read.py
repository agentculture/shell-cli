def test_dot_dot_path_escape_is_refused(env: Environment) -> None:
    outside = env.work_root.parent / "secret.txt"
    outside.write_text("do not read me", encoding="utf-8")

    result = _read(env, {"path": "../secret.txt"})

    assert result.status is OperationStatus.FAILED
    assert "escapes the work root" in result.error


def test_absolute_path_escape_is_refused(env: Environment) -> None:
    outside = env.work_root.parent / "secret2.txt"
    outside.write_text("do not read me either", encoding="utf-8")

    result = _read(env, {"path": str(outside)})

    assert result.status is OperationStatus.FAILED
    assert "escapes the work root" in result.error


def test_symlink_inside_root_pointing_outside_it_is_refused(env: Environment) -> None:
    """The interesting confinement case: the literal path string
    (``link.txt``) never mentions anything outside the root, but resolving
    the symlink lands outside it. A confinement check that only inspects the
    literal path string -- rather than the resolved target -- would miss
    this and let the read through."""
    outside_dir = env.work_root.parent / "outside"
    outside_dir.mkdir()
    secret = outside_dir / "secret.txt"
    secret.write_text("outside content", encoding="utf-8")

    link = env.work_root / "link.txt"
    os.symlink(secret, link)

    result = _read(env, {"path": "link.txt"})

    assert result.status is OperationStatus.FAILED
    assert "escapes the work root" in result.error


def test_symlinked_directory_inside_root_pointing_outside_it_is_refused(
    env: Environment,
) -> None:
    """The same escape, one level up: a symlinked *directory* inside the
    root whose target lives outside it, reached through a nested relative
    path (``linked_dir/secret.txt``)."""
    outside_dir = env.work_root.parent / "outside_dir"
    outside_dir.mkdir()
    (outside_dir / "secret.txt").write_text("outside content", encoding="utf-8")

    link_dir = env.work_root / "linked_dir"
    os.symlink(outside_dir, link_dir, target_is_directory=True)

    result = _read(env, {"path": "linked_dir/secret.txt"})

    assert result.status is OperationStatus.FAILED
    assert "escapes the work root" in result.error


def test_a_normal_symlink_that_stays_inside_the_root_is_readable(env: Environment) -> None:
    """The confinement check must not be so aggressive it refuses a symlink
    that resolves to a target still inside the work root."""
    real = env.work_root / "real.txt"
    real.write_text("hello\n", encoding="utf-8")
    link = env.work_root / "alias.txt"
    os.symlink(real, link)

    result = _read(env, {"path": "alias.txt"})

    assert result.succeeded
    assert result.rendering == "     1\thello"


# --- a handler crash on malformed input: recoverable, never a run abort ---


def test_a_nul_byte_in_the_path_crashes_the_handler_but_is_recovered() -> None:
    """Reproduces colleague's own characterization case
    (``scripts/capture_colleague_baseline.py``, ``execute_wraps_non_tool_error``):
    a NUL byte in the path makes ``Path.resolve()`` raise a bare
    ``ValueError`` deep inside ``_safe_path``. This module deliberately does
    not catch it -- proving that ``operations.execute``'s handler-crash wrap
    (not this module) is what turns it into a recoverable ``FAILED`` result,
    never lets it escape as a raised exception, and records the crash
    honestly rather than as an ordinary policy-driven failure."""
    import tempfile

    with tempfile.TemporaryDirectory() as tmp:
        source = Path(tmp) / "source"
        work = Path(tmp) / "work"
        source.mkdir()
        work.mkdir()
        env = Environment(
            source_root=source,
            work_root=work,
            runner=HostRunner(),
            workspace=WorkspaceKind.WORKTREE,
        )

        records, sink = _sink()
        operation = Operation(kind=fs_read.KIND, arguments={"path": "a\x00b"})

        # The point of the test: this call must not raise.
        result = operations.execute(operation, env, evidence_sink=sink)

    assert result.status is OperationStatus.FAILED
    assert "ValueError" in result.error
    assert fs_read.KIND in result.error

    execution = records[0].to_dict()["execution"]
    assert execution["handler_entered"] is True
    assert execution["handler_disposition"] == "crashed"
    # A crashed handler's "did it apply anything?" is unknowable, not false.
    assert execution["applied"] is None


def test_the_raw_handler_call_is_not_wrapped_without_execute(env: Environment) -> None:
    """Without ``operations.execute``'s wrap, the identical crash escapes as
    a raw, unrecoverable exception -- proof the wrap does real work rather
    than relabelling something that was already safe."""
    operation = Operation(kind=fs_read.KIND, arguments={"path": "a\x00b"})

    with pytest.raises(ValueError):
        fs_read._read(operation, env)


# --- fs.* is not gated by the run_command policy ---------------------------


def test_fs_read_is_ungated_regardless_of_policy(env: Environment) -> None:
    """``fs.*`` is deliberately outside the run_command policy's
    jurisdiction (pinned upstream by
    tests/test_loop_run_command_policy.py:366-386 in colleague) -- confirmed
    here rather than assumed, since a caller could otherwise believe a
    denied policy blocks reads too."""
    from shell.policy import Policy

    (env.work_root / "gated.txt").write_text("content\n", encoding="utf-8")
    # A run_command section present and denying everything -- proves fs.read
    # succeeds even under a policy that would deny any gated operation, since
    # fs.* is outside check_run_command's jurisdiction entirely.
    deny_everything = Policy(
        run_command={"deny": ["anything"]},
        present=frozenset({"run_command"}),
    )

    operation = Operation(kind=fs_read.KIND, arguments={"path": "gated.txt"})
    result = operations.execute(operation, env, policy=deny_everything)

    assert result.status is OperationStatus.SUCCEEDED
    assert result.verdict.decision.value == "ungated"


