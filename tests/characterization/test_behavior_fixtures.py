# --- read_file: numbering happens BEFORE truncation (issue #240) -------
#
# A local reimplementation of colleague's own documented contract
# (colleague/tools.py:599-621, _number_lines) is used ONLY to construct the
# WRONG order for comparison below -- it never substitutes for colleague's
# own numbering, which the captured fixture already pins byte-for-byte.


def _number_lines_reference(text: str, width: int) -> str:
    if text == "":
        return ""
    body = text[:-1] if text.endswith("\n") else text
    lines = body.split("\n")
    return "\n".join(f"{i:{width}d}\t{line}" for i, line in enumerate(lines, start=1))


def _truncate_reference(text: str, limit: int) -> str:
    if len(text) <= limit:
        return text
    return text[:limit] + f"\n... [truncated at {limit} chars]"


def test_read_file_result_is_truncated():
    fixture = _B["read_file_numbers_then_truncates"]
    assert fixture["ok"] is True
    assert len(fixture["input_text"]) > _CONSTANTS["max_output_chars"]
    assert "truncated at" in fixture["result"]


def test_read_file_would_differ_if_numbering_and_truncation_were_reversed():
    """The load-bearing ordering fixture: reimplementing the WRONG order
    (truncate raw text first, number what survives second) must NOT
    reproduce what colleague actually captured. If a future refactor
    reversed the two steps, this test is the one that catches it."""
    fixture = _B["read_file_numbers_then_truncates"]
    raw = fixture["input_text"]
    correct = fixture["result"]

    wrong_order = _number_lines_reference(
        _truncate_reference(raw, fixture["max_output_chars"]),
        fixture["line_number_width"],
    )

    assert wrong_order != correct
    # Concretely: numbering-after-truncation gives the truncation marker
    # itself a phantom line number, because it now runs over text that
    # already contains "... [truncated at N chars]" as if it were real
    # file content. The correct (number-first) order never does this --
    # the marker is appended AFTER numbering completes.
    assert "\t... [truncated at" in wrong_order
    assert "\t... [truncated at" not in correct


def test_read_file_last_visible_line_carries_its_true_file_line_number():
    """A cited line number must be copy-derived from the real file, never
    re-counted from a truncated/windowed remainder (colleague issue #240)."""
    fixture = _B["read_file_numbers_then_truncates"]
    result = fixture["result"]
    body = result.split("\n... [truncated", 1)[0]
    last_line = body.rstrip("\n").split("\n")[-1]
    # "   424\tline00424_..." -- the numeric prefix must match the numeric
    # suffix of the line's own content, proving it is the TRUE file line,
    # not a renumbered position within the truncated chunk.
    number_str, _, content = last_line.partition("\t")
    assert content.startswith(f"line{int(number_str):05d}_")


