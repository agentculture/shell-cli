def test_run_command_truncation_boundary_is_exact():
    """The formatted result is f"exit={code}\\n{stdout}{stderr}" -- the
    7-char "exit=0\\n" prefix counts toward the cap, so the boundary sits at
    max_output_chars - 7 bytes of raw command output, not at max_output_chars
    itself."""
    not_truncated = _B["run_command_truncation_boundary_not_truncated"]
    truncated = _B["run_command_truncation_boundary_truncated"]

    assert not_truncated["ok"] is True
    assert len(not_truncated["result"]) == _CONSTANTS["max_output_chars"]
    assert "truncated at" not in not_truncated["result"]

    assert truncated["ok"] is True
    assert "truncated at 25000 chars" in truncated["result"]
    assert len(truncated["result"]) == _CONSTANTS["max_output_chars"] + len(
        "\n... [truncated at 25000 chars]"
    )


